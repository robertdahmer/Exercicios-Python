pot = n1 ** n2
divint = n1 // n2
resdiv = n1 % n2

print ('A soma dos valores é: {}'.format(soma))
print('A subtração dos valores é: {}'.format(sub))
print('A divisão dos valores é: '.format(div))
print('A multiplicação dos valores é: '.format(mult))
print('A potenciação dos valores é: '.format(pot))
print('A divisão inteira dos valores é: '.format(divint))
print(' O resto da divisão é: '.format(resdiv))
